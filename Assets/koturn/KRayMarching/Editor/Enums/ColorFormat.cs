
namespace Koturn.KRayMarching
{
    /// <summary>
    /// Color Format.
    /// </summary>
    public enum ColorFormat
    {
        /// <summary>
        /// RGB color which components are byte value.
        /// </summary>
        RGB24,
        /// <summary>
        /// RGBA color which components are byte value.
        /// </summary>
        RGBA32,
        /// <summary>
        /// RGB color which components are float value.
        /// </summary>
        RGBFloat,
        /// <summary>
        /// RGBA color which components are float value.
        /// </summary>
        RGBAFloat
    }
}
